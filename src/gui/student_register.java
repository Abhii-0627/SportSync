    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        try {

            String nic = jTextField2.getText();
            String firstname = jTextField6.getText();
            String lastname = jTextField3.getText();
            String email = jTextField1.getText();
            String password = String.valueOf(jPasswordField1.getPassword());
            String mobile = jTextField4.getText();
            String address = jTextField5.getText();
            String gender = String.valueOf(jComboBox1.getSelectedItem());


            if (nic.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please Enter Your NIC!", "warning", JOptionPane.WARNING_MESSAGE);
            } else if (firstname.isEmpty()){
                JOptionPane.showMessageDialog(this, "Please Enter Your First Name!", "warning", JOptionPane.WARNING_MESSAGE);
            } else if (lastname.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please Enter Your Last Name!", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (!email.matches("^(?=.{1,64}@)[A-Za-z0-9\\+_-]+(\\.[A-Za-z0-9\\+_-]+)*@"
                    + "[^-][A-Za-z0-9\\+-]+(\\.[A-Za-z0-9\\+-]+)*(\\.[A-Za-z]{2,})$")) {
                JOptionPane.showMessageDialog(this, "Invalid Email", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (password.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please Enter Your Password!", "Warning", JOptionPane.WARNING_MESSAGE);
//            } else if (!password.matches("^(?=.*[A-Za-z])(?=.*\\d)[A-Za-z\\d]{8,}$")) {
//                JOptionPane.showMessageDialog(this, "Invalid password!", "Warning", JOptionPane.WARNING_MESSAGE);
                
            } else if (mobile.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please Enter Your Mobile!", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (!mobile.matches("^07[01245678]{1}[0-9]{7}$")) {
                JOptionPane.showMessageDialog(this, "Invalide Mobile!", "Warning", JOptionPane.WARNING_MESSAGE);
            
            } else if (address.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Please Enter Address!", "Warning", JOptionPane.WARNING_MESSAGE);
            } else if (gender.equals("Select")) {
                JOptionPane.showMessageDialog(this, "Please Select gender!", "Warning", JOptionPane.WARNING_MESSAGE);
            } else {

                ResultSet resultSet = MySQL2.executeSearch("SELECT * FROM `student` WHERE `email`='" + email + "' OR `mobile`='" + mobile + "' OR `nic`='" + nic + "'");

                if (resultSet.next()) {
                    JOptionPane.showMessageDialog(this, "This user Alredy registered.", "Warning", JOptionPane.WARNING_MESSAGE);

                } else {

                    Date date = new Date();
                    SimpleDateFormat sdf = new SimpleDateFormat("yyy-MM-dd");

                    MySQL2.executeIUD("INSERT INTO `member`(`nic`,`first_name`,`last_name`,`email`,`password`,`mobile`,`address`,`register_date`,`gender_id`)"
                            + "VALUES('" + nic + "','" + firstname + "','" + lastname + "','" + email + "','" + password + "','" + mobile + "','" + address + "',"
                                    + "'" + sdf.format(date) + "','" + employeeGenderMap.get(gender) + "')");
                    
                    loadEmploye();
                    reset();
                }

            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

