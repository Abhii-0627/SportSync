    private void loadGender() {

        try {

            ResultSet resultSet = MySQL2.executeSearch("SELECT * FROM `gender`");

            Vector<String> vector = new Vector<>();
            vector.add("Select");

            while (resultSet.next()) {
                vector.add(resultSet.getString("name"));
                employeeGenderMap.put(resultSet.getString("name"), resultSet.getString("id"));
            }

            DefaultComboBoxModel model = new DefaultComboBoxModel(vector);
            jComboBox1.setModel(model);

        } catch (Exception e) {
            e.printStackTrace();
        }
    }
    
    
    
    
    
    private void loadEmploye() {

        try {

            ResultSet resultSet = MySQL2.executeSearch("SELECT * FROM `member`"
                    + "INNER JOIN `gender` ON `member`.`gender_id` = `gender`.`id`");

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);

            while (resultSet.next()) {
                Vector<String> vector = new Vector<>();
                vector.add(resultSet.getString("id"));                
                vector.add(resultSet.getString("nic"));
                vector.add(resultSet.getString("first_name"));                
                vector.add(resultSet.getString("last_name"));
                vector.add(resultSet.getString("email"));                
                vector.add(resultSet.getString("password"));
                vector.add(resultSet.getString("mobile"));
                vector.add(resultSet.getString("address"));               
                vector.add(resultSet.getString("register_date"));
                vector.add(resultSet.getString("gender.name"));
                

                model.addRow(vector);

            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }

